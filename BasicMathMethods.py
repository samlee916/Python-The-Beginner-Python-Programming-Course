#An example using math methods
import math #implement the math library

num = 5.5
print(math.ceil(num))#rounds up to the highest number

number = 5.4
print(math.floor(number))#rounds to 5
print(round(number))#rounds the number

a = 5.12
print(round(a), 2)#rounds to two decimal places

print(math.sqrt(36))#prints out the 6 because square root of 36 is 6.0

print(math.pow(5,2))#prints out 25 because 5^2 = 25.0
